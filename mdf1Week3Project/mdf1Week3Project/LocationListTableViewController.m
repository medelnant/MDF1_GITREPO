-(void)prepareForSegue:(UIStoryboardSegue *)segue sender:(id)sender {
    if([[segue identifier] isEqualToString:@"showCourseDetail"]) {
        
        //Define Destination View
        LocationDetailViewController *detailView = (LocationDetailViewController *)[segue destinationViewController];
        
        //Pass data to object property
        detailView.locationDataObject = [self.courseList.dataArray objectAtIndex:[[self.tableView indexPathForCell:sender]row]];
        
    }
